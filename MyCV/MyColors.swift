import Foundation
import UIKit

extension UIColor {
    
    static let newOrange: UIColor = {
        return UIColor(red: 252.0/255.0, green: 175.0/255.0, blue: 0.0/255.0, alpha: 180.0)
    }()
    
    static let newWhite: UIColor = {
        return UIColor(red: 255.0/255.0, green: 255.0/255.0, blue: 255.0/255.0, alpha: 180.0)
    }()
}
